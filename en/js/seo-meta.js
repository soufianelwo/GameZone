(function () {
    // Nouveau système — lit le slug depuis l'URL
    function slugifyg(title) {
        return title
            .toLowerCase()
            .normalize("NFD").replace(/[\u0300-\u036f]/g, "")
            .replace(/[^a-z0-9]+/g, "-")
            .replace(/^-+|-+$/g, "");
    }

    const pathParts = window.location.pathname.split('/').filter(Boolean);
    const slug = pathParts[pathParts.length - 1];
    if (!slug || slug === 'game') return;

    // Attend que GAMES soit chargé
    function applyMeta() {
        if (typeof GAMES === 'undefined') {
            setTimeout(applyMeta, 100);
            return;
        }

        const game = GAMES.find(g => slugifyg(g.title) === slug);
        if (!game) return;

        const seo_game = SEO_GAMES[game.id.toString()];

        const title = seo_game.title; //`Jouer à ${game.title} gratuitement – Jeux gratuits`;
        const description = seo_game.description;// `Joue à ${game.title} gratuitement en ligne sur Jeux gratuits. Jeu ${game.category} sans téléchargement, accessible sur PC et mobile.`;
        const canonical = `/en/game/${slug}`;
        const image = `/${game.thumb}`;

        // Title
        document.title = title;

        // Meta description
        let desc = document.querySelector('meta[name="old"]');
        if (!desc) { desc = document.createElement('meta'); desc.setAttribute('name', 'description'); document.head.appendChild(desc); }
        desc.setAttribute('content', description);

        // Canonical
        let can = document.querySelector('link[rel="canonical"]');
        if (!can) { can = document.createElement('link'); can.setAttribute('rel', 'canonical'); document.head.appendChild(can); }
        can.setAttribute('href', canonical);

        // OG tags
        const setMeta = (attr, key, val) => {
            let el = document.querySelector(`meta[${attr}="${key}"]`);
            if (!el) { el = document.createElement('meta'); el.setAttribute(attr, key); document.head.appendChild(el); }
            el.setAttribute('content', val);
        };

        setMeta('property', 'og:title', title);
        setMeta('property', 'og:description', description);
        setMeta('property', 'og:url', canonical);
        setMeta('property', 'og:image', image);
        setMeta('name', 'twitter:title', title);
        setMeta('name', 'twitter:description', description);
        setMeta('name', 'twitter:image', image);

        // H1 et description visible
        const h1 = document.getElementById('game-title-h1');
        if (h1) h1.textContent = `${game.title} – Jeu ${game.category} Gratuit`;
        const p = document.getElementById('game-description');
        if (p) p.textContent = description;

        if (seo_game) {
            document.getElementById('game-title-h1').textContent = seo_game.h1;
            document.getElementById('game-description').textContent = seo_game.description;
            document.getElementById('game-instruction').textContent = seo_game.Instructions;
        }
    }

    applyMeta();
})();
